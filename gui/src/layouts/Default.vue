<script setup>
import {ref ,onMounted, onUnmounted} from 'vue';
import {debounce} from 'lodash';
import {Document, Menu as IconMenu, Setting, Sunny, Moon, Expand, Fold} from '@element-plus/icons-vue';

const innerWidth = ref(window.innerWidth);
const innerHeight = ref(window.innerHeight);
const theme = ref('light');
const activeIndex = ref('0');
const mode = ref(true);
const isCollapse = ref(innerWidth.value < 1200 ? true : false);

if (localStorage.theme === 'dark') {
    document.documentElement.classList.add('dark');
    theme.value = 'dark';
    mode.value = false;
} else {
    document.documentElement.classList.remove('dark');
    theme.value = 'light';
    mode.value = true;
}

const changeMode = (event) => {
    if (mode.value) {
        theme.value = 'light';
    }else{
        theme.value = 'dark'; 
    }
    theme.value === 'light' 
        ? document.documentElement.classList.remove('dark')
        : document.documentElement.classList.add('dark');
    localStorage.theme = theme.value;
};

const handleSelect = (key, keyPath) => {
    // Omit!
    console.log(key, keyPath);
};
const handleOpen = (key, keyPath) => {
    // Omit!
    console.log(key, keyPath);
};
const handleClose = (key, keyPath) => {
    // Omit!
    console.log(key, keyPath);
};
const handleCollapse = (event) => {
    isCollapse.value = !isCollapse.value;
};

const checkWindowSize = () => {
    if (window.innerWidth < 1280) {
        if (isCollapse.value === false && innerWidth.value >= 1280) isCollapse.value = true;
    }else{
        if (isCollapse.value === true) isCollapse.value = false;
    }
    innerWidth.value = window.innerWidth;
    innerHeight.value = window.innerHeight;
};

onMounted(() => {
    window.addEventListener('resize', debounce(checkWindowSize, 100));
});

onUnmounted(() => {
    window.removeEventListener('resize', checkWindowSize);
});
</script>

<style>
.flex-grow {
    flex-grow: 1;
}
</style>

<template>
    <div class="common-layout">
        <el-container>
            <el-aside id="side-menu" :width="isCollapse ? '80px' : '200px'" class="duration-300" >
                <el-menu default-active="1" :collapse="isCollapse" :style="'min-height:'+ innerHeight + 'px'" @open="handleOpen" @close="handleClose">
                    <el-menu-item index="0">
                        LOGO
                    </el-menu-item>
                    <el-sub-menu index="1">
                        <template #title>
                            <el-icon><Document /></el-icon>
                            <span>Navigator One</span>
                        </template>
                        <el-menu-item-group>
                            <template #title><span>Group One</span></template>
                            <el-menu-item index="1-1">Item One</el-menu-item>
                            <el-menu-item index="1-2">Item Two</el-menu-item>
                        </el-menu-item-group>
                        <el-menu-item-group title="Group Two">
                            <el-menu-item index="1-3">Item Three</el-menu-item>
                        </el-menu-item-group>
                        <el-sub-menu index="1-4">
                            <template #title><span>Item Four</span></template>
                            <el-menu-item index="1-4-1">Item One</el-menu-item>
                        </el-sub-menu>
                    </el-sub-menu>
                    <el-menu-item index="2">
                        <el-icon><IconMenu /></el-icon>
                        <template #title><span>Navigator Two</span></template>
                    </el-menu-item>
                    <el-menu-item index="3">
                        <el-icon><Document /></el-icon>
                        <template #title><span>Navigator Three</span></template>
                    </el-menu-item>
                    <el-menu-item index="4">
                        <el-icon><Setting /></el-icon>
                        <template #title><span>Navigator Four</span></template>
                    </el-menu-item>
                </el-menu>
            </el-aside>
            <el-container>
                <el-header>
                    <el-menu :default-active="activeIndex" mode="horizontal" :ellipsis="false" @select="handleSelect">
                        <el-button type="primary" plain size="large" style="margin-left: 4px; margin-top: 10px" @click="handleCollapse">
                            <el-icon v-if="isCollapse"><Expand /></el-icon>
                            <el-icon v-else><Fold /></el-icon>
                        </el-button>
                        <div class="flex-grow" />
                        <el-menu-item index="0">Processing Center</el-menu-item>
                        <el-sub-menu index="1">
                            <template #title>Workspace</template>
                            <el-menu-item index="1-1">Item One</el-menu-item>
                            <el-menu-item index="1-2">Item Two</el-menu-item>
                            <el-menu-item index="1-3">Item Three</el-menu-item>
                            <el-sub-menu index="1-4">
                                <template #title><span>Item Four</span></template>
                                <el-menu-item index="1-4-1">Item One</el-menu-item>
                                <el-menu-item index="1-4-2">Item Two</el-menu-item>
                                <el-menu-item index="1-4-3">Item Three</el-menu-item>
                            </el-sub-menu>
                        </el-sub-menu>
                        <el-switch v-model="mode" @click="changeMode" style="margin-left: 24px; margin-top: 12px;" inline-prompt :active-icon="Sunny" :inactive-icon="Moon" />
                    </el-menu>
                </el-header>
                <el-main>
                    <div>
                        <slot />
                    </div>
                </el-main>
            </el-container>
        </el-container>
    </div>
</template>
